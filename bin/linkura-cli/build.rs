use std::collections::BTreeMap;
use std::env;
use std::fs;
use std::path::{Path, PathBuf};

use anyhow::{Context, Result};
use serde::Deserialize;

#[derive(Debug, Deserialize)]
struct LabelEntry {
    #[serde(rename = "Id")]
    id: u32,
    #[serde(rename = "Label")]
    label: String,
}

fn main() -> Result<()> {
    let manifest_dir =
        PathBuf::from(env::var("CARGO_MANIFEST_DIR").context("CARGO_MANIFEST_DIR is not set")?);
    let repo_root = manifest_dir
        .join("..")
        .join("..")
        .canonicalize()
        .context("failed to resolve repository root")?;

    let live_locations_path = repo_root
        .join("deps")
        .join("link-like-diff")
        .join("LiveLocations.yaml");
    let costumes_path = repo_root
        .join("deps")
        .join("link-like-diff")
        .join("Costumes.yaml");

    println!("cargo:rerun-if-changed={}", live_locations_path.display());
    println!("cargo:rerun-if-changed={}", costumes_path.display());
    println!("cargo:rerun-if-changed=build.rs");

    let live_locations = load_id_to_name_map(&live_locations_path)
        .with_context(|| format!("failed to load {}", live_locations_path.display()))?;
    let costumes = load_id_to_name_map(&costumes_path)
        .with_context(|| format!("failed to load {}", costumes_path.display()))?;

    let generated = generate_source(&live_locations, &costumes);
    let out_dir = PathBuf::from(env::var("OUT_DIR").context("OUT_DIR is not set")?);
    fs::write(out_dir.join("id_to_name_generated.rs"), generated)
        .context("failed to write generated id_to_name source")?;

    Ok(())
}

fn load_id_to_name_map(path: &Path) -> Result<BTreeMap<u32, String>> {
    let content =
        fs::read_to_string(path).with_context(|| format!("failed to read {}", path.display()))?;
    let entries: Vec<LabelEntry> = serde_yaml::from_str(&content)
        .with_context(|| format!("failed to parse YAML {}", path.display()))?;

    let mut map = BTreeMap::new();
    for entry in entries {
        map.insert(entry.id, entry.label);
    }

    Ok(map)
}

fn generate_source(
    live_locations: &BTreeMap<u32, String>,
    costumes: &BTreeMap<u32, String>,
) -> String {
    let mut source = String::from("// @generated by build.rs\n\n");
    source.push_str("pub fn live_location_id_to_name(id: u32) -> Option<&'static str> {\n");
    source.push_str("    match id {\n");
    source.push_str(&generate_match_arms(live_locations));
    source.push_str("        _ => None,\n");
    source.push_str("    }\n");
    source.push_str("}\n\n");

    source.push_str("pub fn costume_id_to_name(id: u32) -> Option<&'static str> {\n");
    source.push_str("    match id {\n");
    source.push_str(&generate_match_arms(costumes));
    source.push_str("        _ => None,\n");
    source.push_str("    }\n");
    source.push_str("}\n");

    source
}

fn generate_match_arms(entries: &BTreeMap<u32, String>) -> String {
    let mut arms = String::new();
    for (id, label) in entries {
        arms.push_str("        ");
        arms.push_str(&id.to_string());
        arms.push_str(" => Some(");
        arms.push_str(&format!("{label:?}"));
        arms.push_str("),\n");
    }
    arms
}
